#OPERAÇÕES ARITMÉTICAS:

print(2+3) #Quando não colocamos aspas nos números, realizamos uma operação. 

print("2+3") #Quando adicionamos aspas, tornamos ela uma string, então neste caso, será retornado 2+3, não o resultado da operação.s

#CONCATENAÇÃO: 
#A concatenação ocorre quando juntamos dois valores. 

print("2" + "3") #Este valor será retornado como: 23, pois usamos aspas e o sinal de +, que neste caso, juntou os dois números. 

print("Hello, " + "Word!") #Aqui estamos concatenando mensagens, nota-se que usamos as aspas e o + como o caso acima. 

print("Hello, " , "Word!") #Neste caso, ao invés do + utilizamos a vírgula. Sendo assim, podemos usar tando o + quanto a vírgula para concatenar mensagens e números. 
 #Ambos os resultados darão "Hello, Word!"

print("O resultado da soma de 2 e 3 é:" , 2+3) #Aqui concatenamos números e texto. Observe que usamos a virgula para realizar a concatenação. 
#O resultado será: "O resultado da soma de 2 e 3 é: 5"

#OPERADORES MATEMÁTICOS NO PYTHON:

#SOMA:
print(2+3) #Usa-se o simbolo de + para realizar somas. 

#MULTIPLICAÇÃO: 
print(2*3) #Usa-se o asteristico para realizar multiplicações.

#DIVISÃO:
print(2/3) #Usa-se uma barra para realizar divisões. 

#SUBTRAÇÃO:
print(2-3) #Usa-se sinal de - para realizar subtração.

#DIVISÃO "INTEIRA":
print(2//3) #Usa-se duas barras para realizar a "divisão inteira", que consiste em pegar somente a parte inteira da divisão. 

#RESTO DA DIVISÃO: 
print(2%3) #Usa-se porcentagem para realizar o resto da divisão. O resto da divisão seria, o que sobra no final dela, não sendo seu resultado.

#POTENCIA:
print(2**3) #Usa-se dois asteristicos para realizar a porcentagem 
